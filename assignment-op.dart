void main(){
  /** Assignment Opeerators
 
 = Simple Assignment
 ??== Assignment the value only if the variable is null
 += Add and assignment
 -= Subtract and Assignment
 *= Multiply and Assignment
 /= Divide and Assignment

 */


// += Add and assignment
  var num = 2;
  num += 3;
  print(num);  // Answer will be 5

// -= Subtract and assignment
  var num3 = 5;
  num3 -= 3;
  print(num3);  // Answer will be 2

// *= Multiply and Assignment
  var num4 = 5;
  num4 *= 3;
  print(num4);  // Answer will be 15

// /= Divide and Assignment
  var num5 = 6;
  num5 /= 3;
  print(num5);  // Answer will be 2
}